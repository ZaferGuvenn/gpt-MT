Autorka 'Rozhovoru s upírem' Anne Rice zemřela ve věku 80 let.
Zemřela kvůli komplikacím vyplývajícím z mrtvice, řekl Christopher Rice.
Největším úspěchem Riceové byl její první román "Interview with the Vampire", který byl publikován v roce 1976 a představil postavu upíra Lestata, který by byl ústřední postavou série 13 knih Chronicles, z nichž poslední byla vydána v roce 2018.
"Měl jsem představu o Lestatovi jako o muži akce, muži, který může dělat věci, které já nemůžu," řekl Rice v přednášce na Southern Illinois University v roce 2010.
"Interview with the Vampire" byl v roce 1994 natočen jako úspěšný celovečerní film, který pomohl znovu oživit zájem o upírský žánr, který pokračoval televizním seriálem "The Vampire Diaries" a filmovou sérií "Twilight".
Ačkoli žila většinu svého života v Kalifornii, Rice byla rodačkou z New Orleans a podle své webové stránky biografie tam umístila mnoho svých příběhů.
Riceův syn Christopher Rice řekl, že byl u matčina lůžka, když zemřela.
