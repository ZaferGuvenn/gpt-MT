Test molekulární diagnostiky může detekovat variantu Omicronu během 20 minut: Zpráva
Korejští vědci vyvinuli molekulární diagnostickou technologii, která může detekovat varianty Omicronu.
Vývoj technologie byl nyní dokončen a očekává se, že bude trvat nějakou dobu, než bude komerčně využit.
POSTECH oznámil 10. den, že výzkumný tým vedený profesorem Lee Jung-wokem z Katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která může detekovat variantu Omicron během 20-30 minut a výsledky bude publikovat online.
Omicron je varianta, ve které se vyskytuje 26-32 mutací ve spike, který se používá k infikování buněk viry COVID-19.
Podle výzkumného týmu může technologie molekulární diagnostiky rozlišovat mutace na úrovni jednotlivých nukleotidů, takže může detekovat "Stealth Omicron", které jsou obtížně detekovatelné PCR testy.
V současné době Korea Centers for Disease Control and Prevention používá tři metody k detekci variant COVID-19: analýzu celého genomu, analýzu cílové DNA (mutace, jako je například protein spike) a PCR test.
V případě varianty Delta ji lze zjistit pomocí současného PCR testu, ale Omicron ne.
Nově vyvinutá technologie tentokrát není metoda sekvenování, která čte sekvence DNA nebo RNA, ale molekulární diagnostická technologie.
Současná technologie zkoumá pouze specifické oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby vyvolávala reakce pouze při přítomnosti RNA COVID-19, čímž umožňuje rychlé zjištění.
Podle profesora Lee má Omicron silný signál pro geny N v testech PCR, ale má slabý signál pro geny S.
V případě "Stealth Omicron" oba N a S geny potvrdily pozitivní výsledek, což ztěžuje jeho odlišení od ostatních variant.
Molekulární diagnostická technologie pracuje na jiných principu než PCR a účinně detekuje variantu Omicron.
Na rozdíl od běžné technologie, která obvykle zpracovává až 96 vzorků na jednotku, nová technologie dokáže zpracovat více než 125 vzorků za 30 minut (více než 250 vzorků za hodinu).
Navíc tato technologie nepotřebuje speciální vybavení, takže může vyrobit diagnostické sady jednoduše a snadno.
Vzhledem k tomu, že metoda může vyvinout diagnostický kit během 4 dnů, očekává se, že bude schopna rychle reagovat i v případě, že v budoucnu vznikne nová varianta nebo virus.
"Doufám, že zveřejnění této technologie nám pomůže co nejdříve se vrátit k normálnímu každodennímu životu," řekl profesor Lee.
Budeme se snažit rychle diagnostikovat a reagovat na nové varianty, které by mohly vyjít po COVID-19.
