Test molekulární diagnostiky může detekovat variantu Omicron během 20 minut: Zpráva
Korejští vědci vyvinuli molekulární diagnostickou technologii, která může detekovat varianty Omicron.
Vývoj technologie byl nyní dokončen a očekává se, že bude trvat čas na její komercializaci.
POSTECH oznámil 10. den, že výzkumný tým vedený profesorem Lee Jung-wook z Katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která může detekovat variantu Omicron během 20-30 minut a výsledky bude publikovat online.
Omicron je varianta, ve které jsou 26-32 mutace ve spike, který se používá k infikování buněk virem COVID-19.
Podle výzkumného týmu může technologie molekulární diagnostiky rozlišovat mutace na jednotlivém nukleotidovém základě, takže může detekovat "Stealth Omicron", které jsou obtížně detekovatelné PCR testy.
V současné době Korea Centers for Disease Control and Prevention používá tři metody k detekci variant COVID-19: analýza celého genomu, analýza cílové DNA (mutace, jako je například protein spike) a PCR test.
V případě varianty Delta ji lze zjistit pomocí současného PCR testu, ale Omicron ne.
Nově vyvinutá technologie tentokrát není metoda sekvenování, která čte sekvence DNA nebo RNA, ale molekulární diagnostická technologie.
Stávající technologie skenuje pouze specifické oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby způsobila reakce pouze při existenci RNA COVID-19, čímž umožňuje rychlé zjištění.
Podle profesora Lee má Omicron silný signál pro N geny v PCR testech, ale má slabý signál pro S geny.
V případě "Stealth Omicron" byly oba geny N a S potvrzeny jako pozitivní, což ztěžuje jeho odlišení od ostatních variant.
Molekulární diagnostická technologie pracuje v různých mechanismech od PCR, účinně detekující Omicron variantu.
Na rozdíl od běžné technologie, která obvykle zpracovává až 96 vzorků na zařízení, nová technologie může zpracovat více než 125 za 30 minut (více než 250 vzorků za hodinu).
Navíc tato technologie nepotřebuje speciální vybavení, takže může vytvářet diagnostické sady jednoduše a snadno.
Protože metoda může vyvinout diagnostický kit během 4 dnů, očekává se, že bude rychle reagovat i v případě, že se v budoucnu objeví nová varianta nebo virus.
"Doufám, že zveřejnění této technologie nám pomůže co nejdříve se vrátit k normálnímu každodennímu životu," řekl profesor Lee.
Budeme se snažit rychle diagnostikovat a reagovat na nové varianty, které by mohly vyjít po COVID-19.
Tato technologie se nyní nachází před komercializací.
Nicméně, může být použit jako pomocný v současných situacích, kde nebyl vyvinut PCR test pro Omicron.
