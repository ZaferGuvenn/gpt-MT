Největším úspěchem Rice bylo její první román "Rozhovor s upírem", který byl vydán v roce 1976 a představil postavu upíra Lestata, který byl hlavní postavou ve 13 knihové sérii Kronik, z nichž nejnovější byla vydána v roce 2018.
"Měl jsem představu o Lestatovi jako o muži akce, muži, který může dělat věci, které já nemůžu," řekl Rice v přednášce na Southern Illinois University v roce 2010.
